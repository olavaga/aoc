from day18 import *

def example():
    ex="""[[[0,[5,8]],[[1,7],[9,6]]],[[4,[1,2]],[[1,4],2]]]
    [[[5,[2,8]],4],[5,[[9,9],0]]]
    [6,[[[6,2],[5,6]],[[7,6],[4,7]]]]
    [[[6,[0,7]],[0,9]],[4,[9,[9,0]]]]
    [[[7,[6,4]],[3,[1,3]]],[[[5,5],1],9]]
    [[6,[[7,3],[3,2]]],[[[3,8],[5,7]],4]]
    [[[[5,4],[7,7]],8],[[8,3],8]]
    [[9,3],[[9,9],[6,[4,9]]]]
    [[2,[[7,7],7]],[[5,8],[[9,3],[0,2]]]]
    [[[[5,2],5],[8,[3,7]]],[[5,[7,5]],[4,4]]]""".split()
    data = []
    for l in ex:
        l = l.replace('[','(')
        l = l.replace(']',')')
        data.append(eval(l))
    return data

            
def test_explode():
    print(explode( (((((9,8),1),2),3),4) ))
    print(explode( (7,(6,(5,(4,(3,2))))) ))
    print(explode( ((6,(5,(4,(3,2)))),1) ))
    print(explode( ((3,(2,(1,(7,3)))),(6,(5,(4,(3,2))))) ))
    print(explode( ((3,(2,(8,0))),(9,(5,(4,(3,2))))) ))

def test_height():
    assert(height( (((((9,8),1),2),3),4) ) == 5)
