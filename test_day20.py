from day20 import count_pixels, transform, img_to_dict

data = "..#.#..#####.#.#.#.###.##.....###.##.#..###.####..#####..#....#..#..##..###..######.###...####..#..#####..##..#.#####...##.#.#..#.##..#.#......#.###.######.###.####...#.##.##..#..#..#####.....#.#....###..#.##......#.....#..#..#..##..#...##.######.####.####.#.#...#.......#..#.#.#...####.##.#......#..#...##.#.##..#...##.#.##..###.#......#.#.......#.#.#.####.###.##...#.....####.#..#..#.##.#....##..#.####....##...##..#...#......#.#.......#.......##..####..#...#.#.#...##..#.#..###..#####........#..####......#..#"

def test_example():
    input_img = \
         """#..#.
            #....
            ##..#
            ..#..
            ..###""".split()
    
    input_data = img_to_dict(input_img)
    assert(count_pixels(input_data) == 10)
    output_data = transform(input_data, (5,5), data)
    assert(count_pixels(output_data) == 24)
    output_data = transform(output_data, (7,7), data)
    assert(count_pixels(output_data) == 35)
