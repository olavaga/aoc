    assert(height( ((6,(5,(4,(3,2)))),1) ) == 5)
    assert(height( ((3,(2,(1,(7,3)))),(6,(5,(4,(3,2))))) ) == 5)
    assert(height( ((3,(2,(8,0))),(9,(5,(4,(3,2))))) ) == 5)

